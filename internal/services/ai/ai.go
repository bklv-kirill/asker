// Package ai — абстракции для ИИ-функционала бота: сборка prompt-строки
// (PromptGenerator) и вызов LLM-провайдера (LLM). Оба интерфейса лежат
// в одном пакете, потому что они оперируют общим типом Prompt и обычно
// используются в паре: PromptGenerator формирует Prompt → LLM его принимает.
//
// Конкретные реализации провайдеров (Anthropic, OpenAI, fake-для-тестов)
// и реализации генератора лежат в подпапках `internal/services/ai/<name>/`,
// каждая в своём пакете. Сборка реализации по cfg.AIProvider — в main.go.
package ai

import (
	"context"

	"github.com/bklv-kirill/asker/internal/models"
)

// Prompt — готовая строка запроса к LLM, собранная PromptGenerator из
// системной обвязки, профиля пользователя, истории диалога и текущего
// вопроса. Named-type над string: исключает случайную передачу
// произвольной строки в LLM.Prompt (и наоборот — Prompt'а туда, где
// ожидался обычный текст); в логах и при явной конвертации `string(p)`
// работает как обычная строка.
type Prompt string

// Info — метаданные LLM-провайдера для логирования и сервисных команд.
// Не предназначено для отображения пользователю напрямую.
type Info struct {
	Provider string
	Model    string
}

// PromptGenerator — контракт сборщика prompt-строки. Чистая функция
// от входных данных: не ходит в БД, не дёргает LLM, не знает про Telegram —
// только форматирует. Сбор истории и профиля — ответственность вызывающего
// слоя (он тянет данные из репозиториев и передаёт сюда).
type PromptGenerator interface {
	// NewPrompt собирает готовый Prompt для LLM по текущему вопросу
	// пользователя.
	//
	// user — профиль пользователя (имя, пол, возраст, инфо), используется
	// для персонализации контекста; ассистент работает только для
	// пользователей с привязанным номером, поэтому user всегда не nil.
	// messages — предыдущие сообщения диалога oldest-first, БЕЗ текущего
	// вопроса (он передаётся отдельно в question). Может быть пустым срезом
	// при первом обращении.
	// question — текущий вопрос пользователя, как пришёл из Telegram.
	NewPrompt(user *models.User, messages []*models.ChatMessage, question string) Prompt
}

// LLM — контракт LLM-провайдера. Потребители (Telegram-хендлер диалога,
// будущие сервисы) зависят от этого интерфейса, а не от конкретной
// реализации; подменить Anthropic на fake для тестов — вопрос сборки.
type LLM interface {
	// GetInfo возвращает метаданные провайдера (имя, модель). Используется
	// в логах при старте и для сервисных команд.
	GetInfo() Info

	// Prompt отправляет один синхронный запрос к LLM и возвращает ответ
	// текстом. prompt — готовый Prompt, собранный вызывающим (обычно
	// реализация PromptGenerator) из системной обвязки, профиля
	// пользователя, истории диалога и текущего вопроса. При сбое
	// сети/таймауте/ошибке провайдера — ошибка обёрнута sentinel'ом
	// конкретной реализации (sentinel'ы определяются в подпакетах).
	Prompt(ctx context.Context, prompt Prompt) (string, error)
}
