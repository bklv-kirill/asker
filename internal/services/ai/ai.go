// Package ai — абстракции для ИИ-функционала бота: вызов LLM-провайдера (LLM).
//
// Конкретные реализации провайдеров (Anthropic, OpenAI, fake-для-тестов)
// лежат в подпапках `internal/services/ai/<name>/`, каждая в своём пакете.
// Сборка реализации по cfg.AIProvider — в main.go.
package ai

import (
	"context"
)

// SystemPrompt — системный промпт ассистента, фиксируется на момент
// конструирования реализации LLM (см. конструкторы провайдеров). Named-type
// над string по той же причине, что и Prompt: исключить путаницу с обычными
// строками и с пользовательским Prompt в сигнатурах конструкторов.
type SystemPrompt string

// Prompt — готовая строка запроса к LLM. Named-type над string: исключает
// случайную передачу произвольной строки в LLM.Prompt (и наоборот — Prompt'а
// туда, где ожидался обычный текст); в логах и при явной конвертации
// `string(p)` работает как обычная строка.
type Prompt string

// Info — метаданные LLM-провайдера для логирования и сервисных команд.
// Не предназначено для отображения пользователю напрямую.
type Info struct {
	Provider string
	Model    string
}

// LLM — контракт LLM-провайдера. Потребители (Telegram-хендлер диалога,
// будущие сервисы) зависят от этого интерфейса, а не от конкретной
// реализации; подменить Anthropic на fake для тестов — вопрос сборки.
type LLM interface {
	// GetInfo возвращает метаданные провайдера (имя, модель). Используется
	// в логах при старте и для сервисных команд.
	GetInfo() Info

	// Prompt отправляет один синхронный запрос к LLM и возвращает ответ
	// текстом. prompt — готовый Prompt, собранный вызывающим из системной
	// обвязки, профиля пользователя, истории диалога и текущего вопроса.
	// При сбое сети/таймауте/ошибке провайдера — ошибка обёрнута sentinel'ом
	// конкретной реализации (sentinel'ы определяются в подпакетах).
	Prompt(ctx context.Context, prompt Prompt) (string, error)
}
