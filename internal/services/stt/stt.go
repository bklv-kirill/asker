// Package stt — абстракции для распознавания речи (Speech-to-Text).
//
// Конкретные реализации провайдеров (Groq Whisper, OpenAI Whisper, fake-для-тестов)
// лежат в подпапках `internal/services/stt/<name>/`, каждая в своём пакете.
// Сборка реализации — в main.go (на текущем масштабе один провайдер,
// фабрика не нужна).
package stt

import (
	"context"
)

// Audio — входной аудиофайл для распознавания. Bytes — содержимое файла,
// MimeType — MIME-тип ("audio/ogg" для voice от Telegram), Filename —
// имя файла, передаётся в multipart-форме провайдеру: некоторые провайдеры
// (Groq, OpenAI) определяют формат по расширению и без него возвращают 400.
type Audio struct {
	Bytes    []byte
	MimeType string
	Filename string
}

// Info — метаданные STT-провайдера для логирования и сервисных команд.
// Не предназначено для отображения пользователю напрямую.
type Info struct {
	Provider string
	Model    string
}

// STT — контракт STT-провайдера. Потребители (Telegram-хендлер голосовых,
// будущие сервисы) зависят от этого интерфейса, а не от конкретной
// реализации; подменить Groq на fake для тестов — вопрос сборки.
type STT interface {
	// GetInfo возвращает метаданные провайдера (имя, модель). Используется
	// в логах при старте и для сервисных команд.
	GetInfo() Info

	// Transcribe отправляет один синхронный запрос на распознавание и
	// возвращает текст. При сбое сети/таймауте/ошибке провайдера — ошибка
	// обёрнута sentinel'ом конкретной реализации (sentinel'ы определяются
	// в подпакетах). Пустая транскрибация (молчание/шум) возвращается как
	// пустая строка без ошибки — решение, что с этим делать, на вызывающем.
	Transcribe(ctx context.Context, audio Audio) (string, error)
}
